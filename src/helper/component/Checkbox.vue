  width: 24px;
  height: 24px;
  cursor: pointer;
  font-size: 24px;
  font-weight: bold;
  border: 1px #9CCC65 solid;
  border-radius: 100px;
  padding: 3px;
  margin-right: 10px;
  color: white;
}

.checked {
  composes: checkbox;
  background: #9CCC65;
}

.noselect {
  -webkit-touch-callout: none;
  -webkit-user-select: none;
  -khtml-user-select: none;
  -moz-user-select: none;
  -ms-user-select: none;
  user-select: none;
}
</style>
