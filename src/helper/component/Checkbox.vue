<template>
<div
  @click="click"
  :class="[this.value ? $style.checked : $style.checkbox, $style.noselect]"
>
  <i v-show="!this.value">no</i>
  <transition name="fade">
    <i
      v-show="this.value"
      class="material-icons"
    >
      check
    </i>
  </transition>
</div>
</template>


<script>
export default {
  name: 'Checkbox',

  props: ['default'],

  data: () => ({
    value: this.default,
  }),

  methods: {
    click: function() {
      this.value = !this.value
      this.$emit('click', this.value)
    },
  },
}
</script>


<style module>
